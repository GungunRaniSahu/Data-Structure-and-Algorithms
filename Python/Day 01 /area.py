# Area of a circle
import math
radius = float(input("Enter radius: "))
area = math.pi * radius**2
print(f"Area of the circle is {area:.2f}")

# Area of a rectangle
length = float(input("Enter length: "))
breadth = float(input("Enter breadth: "))
area = length * breadth
print(f"Area of the rectangle is {area}")
